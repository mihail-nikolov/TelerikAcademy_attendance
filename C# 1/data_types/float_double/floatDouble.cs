using System;


namespace float_double
{
    class FloatDouble
    {
        static void Main()
        {
            float firstVar = 12.345f;
            double secondVar = 3456.091;
            double thirdVar = 34.567839023;
            double fourthVar = 8923.1234857;
            Console.WriteLine("first: {0}", firstVar);
            Console.WriteLine("second: {0}", secondVar);
            Console.WriteLine("third: {0}", thirdVar);
            Console.WriteLine("fourth: {0}", fourthVar);
        }
    }
}
